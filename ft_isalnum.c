int	ft_isalnum(int inp)
{
	if ((inp >= 'A' && inp <= 'Z') || (inp >= 'a' && inp <= 'z'))
		return (1);
	if ((inp >= '0' && inp <= '9'))
		return (1);
	return (0);
}
