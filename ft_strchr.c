char *strchr(const char *s, int c)
{
	int	n;

	n = 0;
	while (*s)
	{
		if (s[n] == c)
		{
			return (s[n]);
			n++;
			while (*s)
			{
				return (s[n]);
				n++;
			}
		}
		n++;
	}
	return (0);
}
